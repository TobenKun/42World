#include "libft.h"

int	ft_atoi(const char *str)
{
	int				i;
	long long int	result;
	int				sign;

	i = 0;
	result = 0;
	sign = 1;
	while ((9 <= str[i] && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
		sign *= -1 + 2 * (str[i++] == '+');
	while ('0' <= str[i] && str[i] <= '9')
		result = result * 10 + (str[i++] - '0');
	return ((int)result * sign);
}
