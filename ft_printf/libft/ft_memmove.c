#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{	
	int	i;

	if (dst + len < src || src + len < dst)
		return (ft_memcpy(dst, src, len));
	else if (dst < src)
	{
		i = 0;
		while (i < (int)len)
		{
			((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
			i++;
		}
		return (dst);
	}
	else if (dst > src)
	{
		i = (int)len - 1;
		while (i >= 0)
		{
			((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
			i--;
		}
		return (dst);
	}
	return (dst);
}
